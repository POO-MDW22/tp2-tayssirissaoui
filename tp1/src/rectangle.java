public class rectangle {
        float largeur;
        float longueur;

        public float perimetre( float n1, float n2) {
            return (n1 * 2 + n2 * 2);
        }

        public float diagonale(float n1, float n2) {
            return (n1 * n1 + n2 * n2);
        }
        public float doubler (float n1, float n2) {
            return (n1 * 2);
        }
}
